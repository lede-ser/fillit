#include "libft.h"

char	*ft_strmapi(char const *s, char (*f) (unsigned int, char))
{
	char	*result;
	int		i;

	i = 0;
	if (s == NULL || f == NULL)
		return (NULL);
	if (!(result = (char*)malloc(sizeof(*result) * ft_strlen(s) + 1)))
		return (NULL);
	while (s[i] != '\0')
	{
		result[i] = f(i, s[i]);
		i++;
	}
	result[i] = '\0';
	return (result);
}
